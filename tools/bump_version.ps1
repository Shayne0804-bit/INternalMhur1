# Auto-increment the Agency (public) build version.
# Called by the BumpAgencyVersion MSBuild target BEFORE ClCompile.
#   - reads version.txt (semver x.y.z), bumps the patch component
#   - writes version.txt back
#   - regenerates src\Core\Version.gen.h with #define RUGIR_AUTOVER "x.y.z"
# SelfUpdate.cpp includes Version.gen.h and reports RUGIR_AUTOVER as its client
# version, so every Agency compile stamps the DLL as the next update.
param([Parameter(Mandatory=$true)][string]$Root)

$verFile = Join-Path $Root 'version.txt'
$genFile = Join-Path $Root 'src\Core\Version.gen.h'

if (Test-Path $verFile) { $v = (Get-Content $verFile -Raw).Trim() } else { $v = '1.0.0' }
if ($v -notmatch '^\d+\.\d+\.\d+$') { $v = '1.0.0' }

$p = $v.Split('.')
$p[2] = [string]([int]$p[2] + 1)
$nv = "$($p[0]).$($p[1]).$($p[2])"

Set-Content -Path $verFile -Value $nv -NoNewline -Encoding ascii

$hdr = @"
// AUTO-GENERATED by tools/bump_version.ps1 on each Agency build. Do NOT edit.
#pragma once
#define RUGIR_AUTOVER "$nv"
"@
Set-Content -Path $genFile -Value $hdr -Encoding ascii

Write-Host "[bump] client version -> $nv"
